package com.tbg.www.thebutterflycorner;

public class ModelCompare {

    String name;
    Double value;

    public ModelCompare(String name, Double value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getValue() {
        return value;
    }

    public void setValue(Double value) {
        this.value = value;
    }
}
